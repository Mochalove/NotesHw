package data_types;

public class Wrapper_Objects {
        public static void main(String[] args) {
        Integer num1 = 15;
        Double double1 = 7.0;
        Character char1 = 'C';
        Float floater = 10.3f;

        System.out.println(num1.intValue());
        System.out.println(double1.doubleValue());
        System.out.println(char1.charValue());
        System.out.println(floater.floatValue());
    }
}
